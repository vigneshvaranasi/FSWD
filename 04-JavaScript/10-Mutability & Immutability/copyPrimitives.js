// Primitives

let a = 5;
// Create Copy
let b = a;
console.log("The value of a is: ", a); // The value of a is: 5
console.log("The value of b is: ", b); // The value of b is: 5

// Mutate
a=a+1;
console.log("The value of a is: ", a); // The value of a is: 6
console.log("The value of b is: ", b); // The value of b is: 5



